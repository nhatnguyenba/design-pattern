/**
 * CD player is one of the audio subsystems.
 */
public class CDPlayer {
    /**
     * Starts playing the CD.
     */
    public void play() {
        System.out.println("Playing CD");
    }

    /**
     * Pauses the CD playback.
     */
    public void pause() {
        System.out.println("Pausing CD");
    }
}